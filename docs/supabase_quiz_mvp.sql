drop policy if exists quiz_session_questions_delete_own on public.quiz_session_questions;
create policy quiz_session_questions_delete_own
on public.quiz_session_questions
for delete
to authenticated
using (
  exists (
    select 1
    from public.quiz_sessions s
    where s.id = quiz_session_questions.session_id
      and s.user_id = auth.uid()
  )
);

